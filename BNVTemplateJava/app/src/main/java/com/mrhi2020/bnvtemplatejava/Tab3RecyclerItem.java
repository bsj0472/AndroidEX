public class Tab3RecyclerItem {
    String imgUrl;
    String title;
    String msg;
    String rating;

    public Tab3RecyclerItem() {
    }

    public Tab3RecyclerItem(String imgUrl, String title, String msg, String rating) {
        this.imgUrl = imgUrl;
        this.title = title;
        this.msg = msg;
        this.rating = rating;
    }
}
